#include <iostream> 
using namespace std;

int main() {
	int begin = 1; // початок діапазону підсумовуваних значень
	int end = 5; // кінець діапазону підсумовуваних значень
	int sum = 0; // змінна для накопичення суми 
	int i = begin; // керуюча змінна циклу

	// перевірка умови
	while (i <= end) {// (порівняння керуючої змінної
		// з кінцем діапазону)
		sum += i; // накопичення суми
		i++; // зміна керуючої змінної
	}

	// показ результату 
	cout << "Result " << sum << "\n\n";

	return 0;
}